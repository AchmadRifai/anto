    private void kodeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_kodeFocusLost
        try {
            Db d = new Db();
            java.sql.PreparedStatement p = d.prep("select kode from barang where kode=?");
            p.setString(1, kode.getText());
            java.sql.ResultSet r = p.executeQuery();
            if (r.next()) {
                JOptionPane.showMessageDialog(rootPane, "Duplikat kode barang!");
                kode.setFocusable(true);
            } r.close();
            p.close();
            d.close();
        } catch (SQLException ex) {
            Db.hindar(ex);
        } refresh();
    }//GEN-LAST:event_kodeFocusLost

