    private void hrgKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_hrgKeyReleased
        if (hrg.isValid() && !"".equals(hrg.getText())) {
            long l = Long.parseLong(hrg.getText());
            if (l <= 0) hrg.setForeground(Color.red);
            else hrg.setForeground(Color.BLACK);
        } refresh();
    }//GEN-LAST:event_hrgKeyReleased

    private void stokKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_stokKeyReleased
        if (stok.isValid() && !"".equals(stok.getText())) {
            double d = Double.parseDouble(stok.getText());
            if (d < 0) stok.setForeground(Color.red);
            else stok.setForeground(Color.BLACK);
        } refresh();
    }//GEN-LAST:event_stokKeyReleased

    private void satKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_satKeyReleased
        if (5 >= sat.getText().length()) sat.setForeground(Color.BLACK);
        else sat.setForeground(Color.red);
        refresh();
    }//GEN-LAST:event_satKeyReleased

