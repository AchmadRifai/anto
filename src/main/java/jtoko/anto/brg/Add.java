    private void refresh() {
        s.setEnabled(!"".equals(kode.getText()) && !"".equals(nm.getText()) && !"".equals(sat.getText()) && !"".equals(ket.getText()) &&
        hrg.isValid() && Color.BLACK == hrg.getForeground() && !"".equals(hrg.getText()) && stok.isValid() && Color.BLACK == 
                stok.getForeground() && !"".equals(stok.getText()) && Color.BLACK == sat.getForeground());
    }

    private void aksekusi() {
        try {
            Db d = new Db();
            aksi(d);
            d.close();
            setVisible(false);
        } catch (SQLException ex) {
            setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
            Db.hindar(ex);
        } reload();
    }

    private void aksi(Db d) throws SQLException {
        java.sql.PreparedStatement p = d.prep("insert into barang values(?,?,0,?,?,?,?,false)");
        p.setString(1, kode.getText());
        p.setString(2, nm.getText());
        p.setLong(3, Long.parseLong(hrg.getText()));
        p.setString(4, ket.getText());
        p.setDouble(5, Double.parseDouble(stok.getText()));
        p.setString(6, sat.getText());
        p.execute();
        p.close();
    }
